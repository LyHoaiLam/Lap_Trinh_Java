package oo.tuan03.buoi06;

public class Day_Book 
{
    private int day;
    private int month;
    private int year;
    
    void set_Day(int day)
    {
        this.day = day;
    }

    int get_Day()
    {
        return this.day;
    }

    void set_Month(int month)
    {
        this.month = month;
    }

    int get_Month()
    {
        return this.month;
    }

    void set_Year(int year)
    {
        this.year = year;
    }

    int get_Year()
    {
        return  this.year;
    }
}
